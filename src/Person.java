public class Person {
//======================================================================================================================
private String nameOfPerson;
private int ageOfPerson;
//----------------------------------------------------------------------------------------------------------------------
    public Person() {

        nameOfPerson = " ";
        ageOfPerson = 0;
    }
//----------------------------------------------------------------------------------------------------------------------
    public Person(String name, int age) {

        ageOfPerson = age;
        nameOfPerson = name;
    }
//----------------------------------------------------------------------------------------------------------------------
    public String toString() {

        return(String.format("%s is %d years old", nameOfPerson, ageOfPerson));
    }
//----------------------------------------------------------------------------------------------------------------------

    public void setAgeOfPerson() {

        ageOfPerson++;
    }
//----------------------------------------------------------------------------------------------------------------------
    public String getNameOfPerson() {

        return nameOfPerson;
    }
//----------------------------------------------------------------------------------------------------------------------
    public int getAgeOfPerson() {

        return ageOfPerson;
    }
//----------------------------------------------------------------------------------------------------------------------
}
//======================================================================================================================